package modelo;

public class CambioMenstruacion {
    
    private String humor;
    private String tipo_dolor;
    private String sensibilidad_emocional;

    public String getHumor() {
        return humor;
    }

    public void setHumor(String humor) {
        this.humor = humor;
    }

    public String getTipo_dolor() {
        return tipo_dolor;
    }

    public void setTipo_dolor(String tipo_dolor) {
        this.tipo_dolor = tipo_dolor;
    }

    public String getSensibilidad_emocional() {
        return sensibilidad_emocional;
    }

    public void setSensibilidad_emocional(String sensibilidad_emocional) {
        this.sensibilidad_emocional = sensibilidad_emocional;
    }
}
